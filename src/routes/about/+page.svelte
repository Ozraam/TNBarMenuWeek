    <h2 class="text-3xl font-bold">About Us</h2>
    <p>Ce site web permet la création des images utilisé pour la communication du menu de semaine du bar de Telecom Nancy.</p>
    
    <section>
        <h3 class="text-2xl font-bold">
            Comment ça marche ?
        </h3>
        <p>
            Le site utilise une API pour générer les images. Il est possible de personnaliser le menu en choisissant les plats et les emplacements.
        </p>
        <p>
            Le site est divisé en deux parties : la partie gauche permet de choisir les plats et la partie droite permet de visualiser le résultat.
        </p>
        <p>
            Il est possible de choisir entre deux emplacements pour les plats : en haut ou en bas de l'image.
        </p>
        <p>
            Une fois le menu personnalisé, il suffit de cliquer sur le bouton "Générer l'image" pour obtenir le résultat.
        </p>
        <p>
            L'image est générée en temps réel et peut être téléchargée en cliquant sur le bouton "Télécharger l'image".
        </p>
        <p>
            Le site est responsive et s'adapte à toutes les tailles d'écran.
        </p>
        <p>
            Le site est développé en Svelte et utilise Tailwind CSS pour le style.
        </p>
    </section>

